package sample;

public class Switchex {

    public static void main(String args[]){

        int n = 10;

        switch (n){
            case 10:
                System.out.println("Value is 10");
                break;
            case 20:
                System.out.println("Value is 20");
                break;
            case 30:
                System.out.println("Value is 30");
                break;
            default:
                System.out.println("Not matching any case values");

        }

    }
}
